package Package_2;

public class Gugudan_3 {

	public static void main(String[] args) {
		int dan;
		int times;
		for(dan=2; dan<=9; dan++) {
			System.out.println(dan+"단 공식");
			for(times=1; times<=9; times++) {
				System.out.println(dan +" X "+ times +" = "+ dan*times);
			}
			System.out.println();
		}
		
	}

}
